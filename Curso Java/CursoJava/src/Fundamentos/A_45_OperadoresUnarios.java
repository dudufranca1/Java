package Fundamentos;

public class A_45_OperadoresUnarios {
	public static void main(String[] args) {
		
		int a = 1;
		int b = 2;
		
		// prefix = incremento ou decremento vem antes da varável ex: --a;
		// posfix = incremento ou decremento vem depois da varável ex: a++;
		
		a++; // incremento a = a + 1
		b--; // decremento a = a - 1
		
		System.out.println(a);
		System.out.println(b);
	}

}
