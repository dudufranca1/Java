package estruturaDeControle;

public class A_62_While {
	public static void main(String[] args) {
		
		// While planejado para ter uma quantidade indeterminada
		// de repetições.
		
		int contador = 0;
		
		while (contador <= 10) {
			System.out.println("Estrutura de repetição While");
			contador++;
		}
	}

}
