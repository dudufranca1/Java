package estruturaDeControle;

public class A_63_For {
	public static void main(String[] args) {
		
		// FOR planejado para ter uma quantidade determinanda
		// de repetições.
		
		//1º criação variável 2º condição da variável 3º incremento.
		for(int cont = 0; cont <= 10; cont++) {
			System.out.printf("i = %d\n",cont);
		}
	}
}
